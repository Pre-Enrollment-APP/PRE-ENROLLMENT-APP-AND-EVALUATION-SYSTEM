package com.example.cccpre_enrollmentapplication;


public class User {

    public String Name, Email, Student_number, Course, Address, Birthday, Contact_Number, Mother, Mother_number, Father, Father_number;

    public User() {

    }

    public User(String Name, String Email, String Student_number, String Course, String Address, String Birthday, String Contact_Number, String Mother, String Mother_number, String Father, String Father_number) {
        this.Name = Name;
        this.Email = Email;
        this.Student_number = Student_number;
        this.Course = Course;
        this.Address = Address;
        this.Birthday = Birthday;
        this.Contact_Number = Contact_Number;
        this.Mother = Mother;
        this.Mother_number = Mother_number;
        this.Father = Father;
        this.Father_number = Father_number;

    }

}